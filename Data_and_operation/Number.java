
public class Number {

	public static void main(String[] args) {
		// + - * / 와 같은 것들은 Operator (연산자)라고 한다.
		System.out.println(6 + 2);
		System.out.println(6 - 2);
		System.out.println(6 * 2);
		System.out.println(6 / 2);
		
		System.out.println(Math.floor(Math.PI));
		System.out.println(Math.ceil(Math.PI));
	}
}
